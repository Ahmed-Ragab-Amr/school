document.addEventListener("DOMContentLoaded", function() {
    // الحصول على جميع نماذج التحميل
    const uploadContainers = document.querySelectorAll('.upload-container');
    const defaultImageSrc = 'user/images/images.png'

    uploadContainers.forEach(container => {
        const imageUpload = container.querySelector('.browse-input');
        const imagePreview = container.querySelector('.upload-icon');

        imageUpload.addEventListener('change', function(event) {
            const file = event.target.files[0];
            if (file) {
                const reader = new FileReader();
                if (file.type.match('image.*')) {
                    reader.onload = function(e) {
                        imagePreview.src = e.target.result;
                        imagePreview.style.display = 'block';
                    }
                    reader.readAsDataURL(file);
                } else if (file.type === 'application/pdf') {
                    imagePreview.src = defaultImageSrc;
                    imagePreview.style.display = 'block';
                } else {
                    alert('Please upload a valid image or PDF file.');
                }
            }
        });
    });
});
